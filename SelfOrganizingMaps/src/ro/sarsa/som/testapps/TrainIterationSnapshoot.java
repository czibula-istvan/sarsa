package ro.sarsa.som.testapps;

import java.util.List;

import ro.sarsa.som.BMU;
import ro.sarsa.som.NeighborSOMNeuron;

public class TrainIterationSnapshoot {
	private BMU bmu;
	private double[] input;
	private List<NeighborSOMNeuron> neighbors;
	private int iteration;

	public TrainIterationSnapshoot(BMU bmu2, double[] input2,
			List<NeighborSOMNeuron> neighbors2, int iteration2) {
		this.bmu = bmu2;
		this.input = input2;
		this.neighbors = neighbors2;
		this.iteration = iteration2;
	}

	public BMU getBMU() {
		return bmu;
	}

	public List<NeighborSOMNeuron> getNeighbors() {
		return neighbors;
	}

	public double[] getInput() {
		return input;
	}

	public int getIteration() {
		return iteration;
	}
}
